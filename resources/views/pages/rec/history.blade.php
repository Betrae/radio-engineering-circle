    Chooks-to-Go was launched by Bounty Agro Ventures, Inc. in 2009. Its name combines "chooks" (the Australian casual term for chicken) with "to-go" to indicate its take-out-only format. To differentiate its products from those of competitors, it is marketed as the only roast chicken that can be enjoyed without the use of sauce (masarap kahit walang sauce). This caters both to the Filipinos' love for family gatherings, and the growing market segment of busy consumers with limited time for food preparation.

    Since its launch, sales have stabilized to near industry-level average volume per store. To reach out to more customers, the company opened more outlets. By the end of 2011, Chooks-to-Go had more stores than the next five most-numerous roast chicken chains combined. In 2012, it was recognized as the top chicken-focused restaurant chain in the country.[2]

    To keep up with the growth, the brand expanded its broiler and breeder operations. Currently, around 200,000 birds are processed daily, with about half of this set aside for Chooks-to-Go outlets. The company targets an annual production of around 100 million birds by the end of 2013.[3]

